/* ---------------------------------------------------------------
   Small helpers
   ---------------------------------------------------------------- */
function useDebounced(value, delay) {
  const [v, setV] = useState(value);
  useEffect(() => {
    const t = setTimeout(() => setV(value), delay);
    return () => clearTimeout(t);
  }, [value, delay]);
  return v;
}

function loadEntries() {
  try {
    const raw = window.localStorage?.getItem?.(STORAGE_KEY);
    if (raw) return JSON.parse(raw);
  } catch (e) {}
  return null;
}

// Composite key for per-season progress tracking.
function entryKey(animeId, seasonNumber) {
  return `${animeId}::s${seasonNumber}`;
}

/* ---------------------------------------------------------------
   Rating stars
   ---------------------------------------------------------------- */
function StarRating({ value, onChange, size = 18, readOnly = false }) {
  const [hover, setHover] = useState(0);
  const stars = [1, 2, 3, 4, 5];
  return (
    <div className="star-row" onMouseLeave={() => setHover(0)}>
      {stars.map((n) => {
        const filled = (hover || value) >= n;
        return (
          <button
            key={n}
            type="button"
            className="star-btn"
            disabled={readOnly}
            onMouseEnter={() => !readOnly && setHover(n)}
            onClick={(e) => {
              e.stopPropagation();
              if (readOnly) return;
              onChange(value === n ? 0 : n);
            }}
            aria-label={`\u041e\u0446\u0435\u043d\u043a\u0430 ${n}`}
          >
            <Star
              size={size}
              strokeWidth={1.6}
              fill={filled ? "#E8A94C" : "none"}
              color={filled ? "#E8A94C" : "#5B5570"}
            />
          </button>
        );
      })}
    </div>
  );
}

