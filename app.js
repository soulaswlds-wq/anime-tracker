/* ---------------------------------------------------------------
   Poster resolution via Jikan (MyAnimeList) API, with an in-memory
   session cache plus a persistent cache so we only ever look each
   title up once, ever, and requests are throttled to respect the
   public rate limit.
   ---------------------------------------------------------------- */
const posterCache = new Map(); // lookup key -> url | null (null = confirmed not found)
const posterListeners = new Map(); // lookup key -> Set of setState callbacks
const posterQueue = []; // pending { key, query, year } jobs, throttled
const posterInFlight = new Set(); // keys already queued/fetching, to dedupe
let posterQueueRunning = false;

function subscribePoster(key, cb) {
  if (!posterListeners.has(key)) posterListeners.set(key, new Set());
  posterListeners.get(key).add(cb);
  return () => posterListeners.get(key)?.delete(cb);
}

function notifyPoster(key, url) {
  posterCache.set(key, url);
  posterListeners.get(key)?.forEach((cb) => cb(url));
}

function posterStorageKey(key) {
  return `poster-url:${key}`;
}

async function loadPersistedPoster(key) {
  try {
    const raw = window.localStorage?.getItem?.(posterStorageKey(key));
    if (raw !== null && raw !== undefined) return JSON.parse(raw);
  } catch (e) {}
  return undefined; // not cached
}

async function persistPoster(key, url) {
  try {
    window.localStorage?.setItem?.(posterStorageKey(key), JSON.stringify(url));
  } catch (e) {}
}

async function runPosterQueue() {
  if (posterQueueRunning) return;
  posterQueueRunning = true;
  while (posterQueue.length > 0) {
    const { key, query, year } = posterQueue.shift();

    // Persistent cache first \u2014 avoids re-hitting the API across sessions.
    const persisted = await loadPersistedPoster(key);
    if (persisted !== undefined) {
      notifyPoster(key, persisted);
      continue;
    }

    try {
      const q = encodeURIComponent(query);
      const res = await fetch(`https://api.jikan.moe/v4/anime?q=${q}&limit=5`);
      if (!res.ok) throw new Error("jikan error");
      const data = await res.json();
      const candidates = data?.data || [];

      const best =
        candidates.find((c) => {
          const y = c.year || c.aired?.prop?.from?.year;
          return y && Math.abs(y - year) <= 1;
        }) || candidates[0];

      const url =
        best?.images?.webp?.large_image_url ||
        best?.images?.jpg?.large_image_url ||
        best?.images?.webp?.image_url ||
        best?.images?.jpg?.image_url ||
        null;

      notifyPoster(key, url);
      persistPoster(key, url);
    } catch (e) {
      notifyPoster(key, null);
    }
    // Stay well under Jikan's public rate limit (~3 req/sec, 60/min).
    await new Promise((r) => setTimeout(r, 500));
  }
  posterQueueRunning = false;
}

function fetchPosterFromJikan(key, query, year) {
  if (posterCache.has(key) || posterInFlight.has(key)) return;
  posterInFlight.add(key);
  posterQueue.push({ key, query, year });
  runPosterQueue();
}

/* ---------------------------------------------------------------
   Poster image with graceful fallback. Resolves per *season* so
   sequels get their own key art instead of reusing season 1's.
   ---------------------------------------------------------------- */
function Poster({ title, original, year }) {
  const key = original || title;
  const [src, setSrc] = useState(() => posterCache.get(key) || null);
  const [errored, setErrored] = useState(false);

  useEffect(() => {
    setErrored(false);
    if (posterCache.has(key)) {
      setSrc(posterCache.get(key));
      return;
    }
    setSrc(null);
    const unsub = subscribePoster(key, setSrc);
    fetchPosterFromJikan(key, original || title, year);
    return unsub;
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [key]);

  if (errored || !src) {
    const initials = title
      .split(" ")
      .slice(0, 2)
      .map((w) => w[0])
      .join("")
      .toUpperCase();
    return (
      <div className="poster poster-fallback">
        <span className="poster-fallback-glyph">{initials}</span>
        <div className="poster-fallback-noise" />
      </div>
    );
  }

  return (
    <div className="poster">
      <img src={src} alt={title} loading="lazy" onError={() => setErrored(true)} />
      <div className="poster-sheen" />
    </div>
  );
}

