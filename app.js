/* ---------------------------------------------------------------
   Main App
   ---------------------------------------------------------------- */
function App() {
  const [entries, setEntries] = useState({});
  const [activeSeasons, setActiveSeasons] = useState({}); // animeId -> season number currently shown
  const [loaded, setLoaded] = useState(false);
  const [activeTab, setActiveTab] = useState("all");
  const [query, setQuery] = useState("");
  const [openId, setOpenId] = useState(null);
  const debouncedQuery = useDebounced(query, 150);

  // Load persisted data from localStorage (survives app restarts on-device)
  useEffect(() => {
    let cancelled = false;
    function init() {
      const data = loadEntries();

      const base = {};
      SEED_ANIME.forEach((a) => {
        a.seasons.forEach((s) => {
          base[entryKey(a.id, s.number)] = { status: "planned", rating: 0, note: "", date: "" };
        });
      });
      const merged = { ...base, ...(data || {}) };

      const initialSeasons = {};
      SEED_ANIME.forEach((a) => {
        initialSeasons[a.id] = a.seasons[0].number;
      });

      if (!cancelled) {
        setEntries(merged);
        setActiveSeasons(initialSeasons);
        setLoaded(true);
      }
    }
    init();
    return () => {
      cancelled = true;
    };
  }, []);

  // Persist on every change
  useEffect(() => {
    if (!loaded) return;
    try {
      window.localStorage?.setItem?.(STORAGE_KEY, JSON.stringify(entries));
    } catch (e) {}
  }, [entries, loaded]);

  function updateEntry(animeId, seasonNumber, patch) {
    const key = entryKey(animeId, seasonNumber);
    setEntries((prev) => ({ ...prev, [key]: { ...prev[key], ...patch } }));
  }

  function setSeason(animeId, seasonNumber) {
    setActiveSeasons((prev) => ({ ...prev, [animeId]: seasonNumber }));
  }

  // Overall status of a title = status of its most recent season with any progress,
  // falling back to the first season's status. Used for tab counts/filtering.
  function overallStatus(anime) {
    for (let i = anime.seasons.length - 1; i >= 0; i--) {
      const s = entries[entryKey(anime.id, anime.seasons[i].number)];
      if (s && s.status !== "planned") return s.status;
    }
    const first = entries[entryKey(anime.id, anime.seasons[0].number)];
    return first?.status || "planned";
  }

  const counts = useMemo(() => {
    const c = { watching: 0, planned: 0, completed: 0 };
    SEED_ANIME.forEach((a) => {
      const st = overallStatus(a);
      if (c[st] !== undefined) c[st] += 1;
    });
    return c;
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [entries]);

  const filtered = useMemo(() => {
    let list = SEED_ANIME;
    if (activeTab !== "all") list = list.filter((a) => overallStatus(a) === activeTab);
    if (debouncedQuery.trim()) {
      const q = debouncedQuery.trim().toLowerCase();
      list = list.filter(
        (a) =>
          a.title.toLowerCase().includes(q) ||
          a.seasons.some((s) => s.original.toLowerCase().includes(q)) ||
          a.genres.some((g) => g.toLowerCase().includes(q))
      );
    }
    return list;
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [activeTab, debouncedQuery, entries]);

  const openAnime = openId ? SEED_ANIME.find((a) => a.id === openId) : null;

  if (!loaded) {
    return (
      <div className="app-loading">
        <div className="loading-spinner" />
      </div>
    );
  }

  return (
    <div className="app">
      <style>{CSS}</style>

      <header className="header">
        <div className="header-inner">
          <div className="header-title-block">
            <p className="header-eyebrow">\u041b\u0438\u0447\u043d\u0430\u044f \u043a\u043e\u043b\u043b\u0435\u043a\u0446\u0438\u044f</p>
            <h1 className="header-title">\u0427\u0442\u043e \u0441\u043c\u043e\u0442\u0440\u044e</h1>
          </div>
          <div className="search-box">
            <Search size={15} strokeWidth={2} />
            <input
              type="text"
              placeholder="\u041f\u043e\u0438\u0441\u043a \u043f\u043e \u043d\u0430\u0437\u0432\u0430\u043d\u0438\u044e \u0438\u043b\u0438 \u0436\u0430\u043d\u0440\u0443..."
              value={query}
              onChange={(e) => setQuery(e.target.value)}
            />
          </div>
        </div>

        <nav className="tabs">
          <button className={`tab ${activeTab === "all" ? "active" : ""}`} onClick={() => setActiveTab("all")}>
            \u0412\u0441\u0435 <span className="tab-count">{SEED_ANIME.length}</span>
          </button>
          <button
            className={`tab ${activeTab === "watching" ? "active" : ""}`}
            onClick={() => setActiveTab("watching")}
            style={{ "--tab-accent": STATUS.watching.color }}
          >
            \u0421\u043c\u043e\u0442\u0440\u044e <span className="tab-count">{counts.watching}</span>
          </button>
          <button
            className={`tab ${activeTab === "completed" ? "active" : ""}`}
            onClick={() => setActiveTab("completed")}
            style={{ "--tab-accent": STATUS.completed.color }}
          >
            \u041f\u0440\u043e\u0441\u043c\u043e\u0442\u0440\u0435\u043d\u043e <span className="tab-count">{counts.completed}</span>
          </button>
          <button
            className={`tab ${activeTab === "planned" ? "active" : ""}`}
            onClick={() => setActiveTab("planned")}
            style={{ "--tab-accent": STATUS.planned.color }}
          >
            \u0411\u0443\u0434\u0443 \u0441\u043c\u043e\u0442\u0440\u0435\u0442\u044c <span className="tab-count">{counts.planned}</span>
          </button>
        </nav>
      </header>

      <main className="grid-wrap">
        {filtered.length === 0 ? (
          <div className="empty-state">
            <p>\u041d\u0438\u0447\u0435\u0433\u043e \u043d\u0435 \u043d\u0430\u0448\u043b\u043e\u0441\u044c. \u041f\u043e\u043f\u0440\u043e\u0431\u0443\u0439 \u0434\u0440\u0443\u0433\u043e\u0439 \u0437\u0430\u043f\u0440\u043e\u0441 \u0438\u043b\u0438 \u0432\u043a\u043b\u0430\u0434\u043a\u0443.</p>
          </div>
        ) : (
          <div className="grid">
            {filtered.map((a) => {
              const activeSeason = activeSeasons[a.id] || a.seasons[0].number;
              const entry = entries[entryKey(a.id, activeSeason)] || { status: "planned", rating: 0, note: "", date: "" };
              return (
                <Card
                  key={a.id}
                  anime={a}
                  activeSeason={activeSeason}
                  onSeasonChange={setSeason}
                  entry={entry}
                  onOpen={setOpenId}
                  onStatusChange={(id, season, s) => updateEntry(id, season, { status: s })}
                  onRatingChange={(id, season, r) => updateEntry(id, season, { rating: r })}
                />
              );
            })}
          </div>
        )}
      </main>

      {openAnime &&
        (() => {
          const activeSeason = activeSeasons[openAnime.id] || openAnime.seasons[0].number;
          const entry = entries[entryKey(openAnime.id, activeSeason)] || {
            status: "planned",
            rating: 0,
            note: "",
            date: "",
          };
          return (
            <DetailModal
              anime={openAnime}
              activeSeason={activeSeason}
              onSeasonChange={setSeason}
              entry={entry}
              onClose={() => setOpenId(null)}
              onStatusChange={(id, season, s) => updateEntry(id, season, { status: s })}
              onRatingChange={(id, season, r) => updateEntry(id, season, { rating: r })}
              onNoteChange={(id, season, note) => updateEntry(id, season, { note })}
              onDateChange={(id, season, date) => updateEntry(id, season, { date })}
            />
          );
        })()}
    </div>
  );
}

